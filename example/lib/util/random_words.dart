import 'dart:math';

String randomWords({
  int minLength = 0,
  required int maxLength,
}) {
  String result = '';
  final r = Random();
  final length = r.nextInt(maxLength - minLength) + minLength;
  for (int i = 0; i < length; i++) {
    result += it_500[r.nextInt(it_500.length)];
  }
  return result;
}

